class sphere {
    public static void main(String[] args) {
        double r = 5.0;
        double volume = 4 / 3 * Math.PI * (r * r * r);
        System.out.println("Volume of the Sphere is " + volume);
        double sur_area = 4 * 3.14 * (r * r);
        System.out.println("Surface Area of the Sphere is " + sur_area);

    }
}
